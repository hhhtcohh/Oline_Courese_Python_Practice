# 算术运算符 : +  -  *  /  //  %  **
print("10 + 4 = ", 10 + 4)  # 加
print("10 - 4 = ", 10 - 4)  # 减
print("10 * 4 = ", 10 * 4)  # 乘
print("10 / 4 = ", 10 / 4)  # 除 - 2.5
print("10 // 4 = ", 10 // 4) # 整除(结果为整数) - 2
print("10 % 4 = ", 10 % 4)  # 取余/求模 - 2
print("10 ** 4 = ", 10 ** 4) # 幂指数, 10的4次方 - 10000

# # 算术运算符的优先级 --> **  --> *  /  //  %   --> +  -
print("0.1 + 10 / 4**2 = ", 0.1 + 10 / 4 ** 2)

